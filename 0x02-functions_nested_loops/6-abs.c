#include "main.h"

/**
 * _abs - Entry point for function
 * @int: parameter type
 *
 * Desc: a function that computes the absolute value of an integer
 * Return: Always 0 (Success)
 */
int _abs(int)
{
	if (int < 0)
	{
		int abs = 0 - int;

		_putchar(abs + '0');
	}
	else
	{
		_putchar(int + '0');
	}

	return (0);
}
